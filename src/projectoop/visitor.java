         String yea=Integer.toString(year);
        int day =cal.get( Calendar.DAY_OF_MONTH);
         String da=Integer.toString(day);
          dat=year+"/"+(month+1)+"/"+day;
        
         date.setText(year+"/"+(month+1)+"/"+day);
        
